"""
Bucle FOR: version 1 del bucle:
    - Recorrer una serie de valores de un conjunto
"""

semana = ["Lunes","Martes","Miercoles","Jueves","Viernes","Sabado","Domingo"]
for dia in (semana):
    print (dia) #imprimimos los valores de la semana
print("Fin de la semana y del bucle for")


